# 将如下数据形成一个字典season
# 键            值
# 1        "春季有1,2,3月"
# 2        "夏季有4,5,6月"
# 3        "秋季有7,8,9月"
# 4        "冬季有10,11,12月"
# 让用户输入一个整数,代表季度,打印这个季度的信息,
# 如果用户输入的信息不存于字典中,则打印"信息不存在"
d={}
d[1]="春季有1,2,3月"
d[2]="夏季有4,5,6月"
d[3]="秋季有7,8,9月"
d[4]="冬季有10,11,12月"
print(d)
s=int(input("请输入季度:"))
if 1<=s<=4:
    print(s,"季度是:",d[s])
else:
    print("信息不存在!")

